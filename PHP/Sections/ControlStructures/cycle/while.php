<?php
//первая строка: | 1 * 1 = 1 |, | 1 * 2 = 2 |, | 1 * 3 = 3 |
//вторая строка: | 2 * 1 = 2 |, | 2 * 2 = 4 |, | 2 * 3 = 6 |
//до девятой строки
echo "<table border='1'>\n";
$i = 1;
while ($i <= 9) {
    echo "\t<tr>\n";
    $j = 1;
    while ($j <= 9) {
        //Чтобы оператор умножения сработал, необходимо использовать * не в строке
        echo "\t\t<td>$i * $j = ".$i * $j."</td>\n";
        $j++;
    }
    echo "\t</tr>\n";
    $i++;
}
echo "</table>\n";